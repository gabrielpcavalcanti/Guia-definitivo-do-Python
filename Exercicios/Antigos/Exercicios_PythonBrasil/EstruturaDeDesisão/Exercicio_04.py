"""
Faça um Programa que verifique se uma letra digitada é vogal ou consoante.
"""

letra = str(input("Digite uma letra: ").upper())

print()

if letra == 'A':
    print("É uma vogal")
elif letra == 'E':
    print("É uma vogal")
elif letra == 'I':
    print("É uma vogal")
elif letra == 'O':
    print("É uma vogal")
elif letra == 'U':
    print("É uma vogal")
else:
    print("É uma consoante")
